#include <stdio.h>

int main() {
    // ใช้ for loop เพื่อแสดงเลขคู่จาก 2 ถึง 10
    for (int i = 2; i <= 10; i += 2) {
        printf("%d ", i);
    }
    printf("\n End of loop\n");
    // คำอธิบาย:
    // โปรแกรมนี้ใช้ for loop โดยกำหนดค่าเริ่มต้น i = 2
    // ตรวจสอบเงื่อนไข i <= 10 ถ้าเป็นจริงจะแสดงค่า i แล้วเพิ่มค่า i ทีละ 2
    // ทำซ้ำจนกว่า i จะมากกว่า 10
    return 0;
}
