#include <stdio.h>

int main() {
    int i = 2;
    // ใช้ while loop เพื่อแสดงเลขคู่จาก 2 ถึง 10
    while (i <= 10) {
        printf("%d ", i);
        i += 2; // เพิ่มค่า i ทีละ 2 เพื่อให้ได้เลขคู่ถัดไป
    }
    printf("\nEnd of loop\n");
    // คำอธิบาย:
    // โปรแกรมนี้เริ่มต้นที่ i = 2 แล้วใช้ while loop ตรวจสอบว่า i <= 10 หรือไม่
    // ถ้าใช่จะแสดงค่า i แล้วเพิ่มค่า i ทีละ 2 จนกว่า i จะมากกว่า 10
    return 0;
}
